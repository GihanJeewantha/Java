
public class Main {

	public static void main(String[] args) {
		
		//expression = operands & operators
		//operands = values , variable , number , quantity
		//operator = + , - , * , / , %
		
		int friends = 10;
		
		friends = friends + 2;
		System.out.println(friends);
		
		friends = friends * 2;
		System.out.println(friends);
		
		friends = friends / 2;
		System.out.println(friends);
		friends = friends % 2;
		System.out.println(friends);
		
		//using increment operator
		
		friends = 0;
		friends ++;
		System.out.println(friends);
		friends --;
		System.out.println(friends);
		
		double number = 20 / 3;
		System.out.println(number);

	}

}
